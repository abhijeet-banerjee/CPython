class Teacher(object):
    '''
    Mutators methods
    '''
    def setName(self,name):
        self.name = name
    def getName(self):
        return self.name

    def setId(self,idt):
        self.idt = idt
    def getId(self):
        return self.idt

    def setAddr(self,addr):
        self.addr = addr
    def getAddr(self):
        return self.addr